void matrix_transform(glm::mat4 &mat) {
	for (int i = 0; i < 4; i++) {
		for (int j = 0; j < 4; j++) {
			if (j == i)
				mat[i][j] = 1.0f;
			else
				mat[i][j] = 0.0f;
		}
	}
}

